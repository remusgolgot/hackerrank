    static class CustomComparator implements Comparator<String> {
        @Override
        public int compare(String s1, String s2) {
            if (getCardValue(s1) < getCardValue(s2))
                return -1;
            if (getCardValue(s1) > getCardValue(s2))
                return 1;
            if (getCardValue(s1) == getCardValue(s2)) {
                for (int i = 0; i < s1.length(); i++) {
                    int v1 = getCardScore(s1.charAt(i));
                    int v2 = getCardScore(s2.charAt(i));
                    if (v1 < v2) {
                        return -1;
                    }
                    if (v1 > v2) {
                        return 1;
                    }
                }
            }
            return 0;
        }

        private int getCardScore(char charAt) {
            int v1 = 0;
            if (charAt == 'J') v1 = 1;
            if (charAt == '2') v1 = 2;
            if (charAt == '3') v1 = 3;
            if (charAt == '4') v1 = 4;
            if (charAt == '5') v1 = 5;
            if (charAt == '6') v1 = 6;
            if (charAt == '7') v1 = 7;
            if (charAt == '8') v1 = 8;
            if (charAt == '9') v1 = 9;
            if (charAt == 'T') v1 = 10;
            if (charAt == 'Q') v1 = 12;
            if (charAt == 'K') v1 = 13;
            if (charAt == 'A') v1 = 14;
            return v1;
        }
    }

    static int getCardValue(String card) {
        char[] ch = card.toCharArray();
        Map<String, Integer> map = new HashMap<>();
        int k = 1;
        int countJ = 0;
        for (int i = 0; i < ch.length; i++) {
            String substring = card.substring(i, i + 1);
            Integer integer = map.get(substring);
            if (integer == null) {
                integer = 0;
            }
            int value = integer + 1;
            map.put(substring, value);
            k *= value;
            if (substring.equals("J")) {
                countJ++;
            }
        }
        if (k == 1 && map.size() == 5) {
            if (countJ == 0) {
                return 1; // high card
            }
            if (countJ == 1) {
                return 2; // one pair (with joker)
            }
        }
        if (k == 2 && map.size() == 4) {
            if (countJ == 0) {
                return 2; // one pair
            }
            if (countJ == 1) {
                return 4; // three of a kind (one pair + joker)
            }
            if (countJ == 2) {
                return 4; // three of a kind (two jokers + one more card)
            }

        }
        if (k == 4 && map.size() == 3) {
            if (countJ == 0) {
                return 3; // two pair
            }
            if (countJ == 1) {
                return 5; // full house (two pair + joker)
            }
            if (countJ == 2) {
                return 6; // four of a kind (two pair + 2 jokers)
            }
        }
        if (k == 6 && map.size() == 3) {
            if (countJ == 0) {
                return 4; // three of a kind
            }
            if (countJ == 1) {
                return 6; // four of a kind (three of a kind + joker)
            }
            if (countJ == 3) {
                return 6; // four of a kind (three jokers + card)
            }

        }
        if (k == 12 && map.size() == 2) {
            if (countJ == 0) {
                return 5; // full house
            }
            if (countJ == 2 || countJ == 3) {
                return 7; // five of a kind
            }
        }
        if (k == 24 && map.size() == 2) {
            if (countJ == 0) {
                return 6; // four of a kind
            } else {
                return 7;
            }
        }
        if (k == 120 && map.size() == 1) return 7; // five of a kind

        return 0;
    }

