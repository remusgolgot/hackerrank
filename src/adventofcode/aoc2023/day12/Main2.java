    private static String foldString2(String x) {
        String res = "";
        for (int i = 0; i < 5; i++) {
            res = res + x;
            if (i < 4) {
                res += ",";
            }
        }
        return res;
    }

    private static String foldString1(String x) {
        String res = x;
        for (int i = 0; i < 5; i++) {
            res = res + x;
            if (i < 4) {
                res += "?";
            }
        }
        return res;
    }

    private static int check(String xx, String f) {
        String[] xxx = xx.split("\\.");
        String[] fs = f.split(",");
        int k = 0;
        List<String> xxxx = new ArrayList<>();
        for (int i = 0; i < xxx.length; i++) {
            if (xxx[i].length() > 0) {
                xxxx.add(xxx[i]);
            }
        }
        if (xxxx.size() != fs.length) {
            return 0;
        }

        for (String s : xxxx) {
            if (s.length() > 0 && k < fs.length) {
                if (Integer.parseInt(fs[k]) != s.length()) {
                    return 0;
                }
                k++;
            }
        }
//        System.out.println(xx + " " + f);
        return 1;

    }

    private static int recursive(String x, String f, int i) {

//        System.out.println(x);
        if (i == x.length()) {
            return check(x, f);
        }

        if (x.charAt(i) == '.' || x.charAt(i) == '#') {
            return recursive(x, f, i + 1);
        }

        return recursive(x.substring(0, i) + "#" + x.substring(i + 1), f, i) + recursive(x.substring(0, i) + "." + x.substring(i + 1), f, i);
    }


