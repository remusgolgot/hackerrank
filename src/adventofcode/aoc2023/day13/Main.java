            "..#.#.##.#.\n" +
            "#..#....#..\n" +
            "....##.###.\n" +
            "..#.##...##\n" +
            ".....#..#.#\n";

    public static void main(String[] args) {

        String[] ss = sq.split("\n");
        int sum = 0;
        for (int i = 0; i < ss.length; i++) {
            List<String> tt = new ArrayList<>();
            while (i < ss.length && !ss[i].equals("")) {
                tt.add(ss[i]);
                i++;
            }
            for (int j = 0; j < tt.size() - 1; j++) {
                if (tt.get(j).equals(tt.get(j + 1)) && allRowsReflect(tt, j)) {
                    sum += 100 * (j + 1);
                }
            }
            String[] uu = rotate(tt);
            for (int j = 0; j < uu.length - 1; j++) {
                if (uu[j].equals(uu[j + 1]) && allRowsReflect(Arrays.asList(uu), j)) {
                    sum += (j + 1);
                }
            }
            System.out.println(sum);
        }
        System.out.println(sum);
    }

    private static boolean allRowsReflect(List<String> tt, int j) {
        int k = j + 1;
        while (j >= 0 && k < tt.size()) {
            if (!tt.get(j).equals(tt.get(k))) {
                return false;
            }
            j--;
            k++;
        }
        return true;
    }

    private static String[] rotate(List<String> tt) {

        String[] res = new String[tt.get(0).length()];
        Arrays.fill(res, "");
        for (int i = 0; i < tt.size(); i++) {
            for (int j = 0; j < tt.get(0).length(); j++) {
                res[j] = res[j] + tt.get(i).charAt(j);
            }
        }

        return res;
    }

